CREATE SEQUENCE SEQ_EGPT_REBATE_PERIOD;

CREATE TABLE EGPT_REBATE_PERIOD
(
	ID			BIGINT NOT NULL,
	INSTALLMENT		BIGINT NOT NULL,
	REBATEDATE		TIMESTAMP WITHOUT TIME ZONE NOT NULL,
	CREATEDBY		BIGINT NOT NULL,
	CREATEDDATE     	TIMESTAMP WITHOUT TIME ZONE       NOT NULL,
	LASTMODIFIEDDATE	TIMESTAMP WITHOUT TIME ZONE       NOT NULL,
	LASTMODIFIEDBY		BIGINT NOT NULL,
	VERSION 		BIGINT,
	CONSTRAINT PK_EGPT_REBATE_ID PRIMARY KEY (ID ),
	CONSTRAINT FK_EGPT_REBATE_INST_ID FOREIGN KEY (INSTALLMENT) REFERENCES EG_INSTALLMENT_MASTER (ID),
	CONSTRAINT FK_EGPT_REBATE_CREATEDBY FOREIGN KEY (CREATEDBY) REFERENCES EG_USER (ID),
	CONSTRAINT FK_EGPT_REBATE_LASTMODIFIEDBY FOREIGN KEY (LASTMODIFIEDBY) REFERENCES EG_USER (ID)
);


